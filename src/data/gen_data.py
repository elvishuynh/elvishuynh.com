import csv
import io

def parse_csv(csv_str):
    reader = csv.reader(io.StringIO(csv_str.strip()), delimiter=',')
    data = []
    for row in reader:
        if len(row) >= 2 and row[0].startswith("2025"):
            data.append({"date": row[0], "value": int(row[1])})
    return data

